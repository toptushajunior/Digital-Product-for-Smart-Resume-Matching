# Функция для создания и отображения облаков слов в виде матрицы 2x3 без заголовков
def plot_common_words_matrix_2x3(tfidf_matrix, feature_names, doc_index, rows=2, cols=3):
    fig, axes = plt.subplots(rows, cols, figsize=(18, 8))  # Новый размер под матрицу 2x3
    axes = axes.flatten()

    for i in range(1, 7):  # Отображение только для первых шести документов
        # Получение ненулевых значений TF-IDF для обоих документов
        doc_words = tfidf_matrix.iloc[0, :].to_numpy().nonzero()[0]
        compare_words = tfidf_matrix.iloc[i, :].to_numpy().nonzero()[0]

        # Поиск общих слов (пересечение)
        common_words_idx = list(set(doc_words) & set(compare_words))

        # Словарь слов и их TF-IDF значений для облака слов
        common_words_scores = {feature_names[idx]: tfidf_matrix.iloc[i, idx] for idx in common_words_idx}

        # Создание облака слов
        wordcloud = WordCloud(width=400, height=400, background_color='white')
        wordcloud.generate_from_frequencies(common_words_scores)

        axes[i-1].imshow(wordcloud, interpolation='bilinear')
        axes[i-1].axis('off')  # Скрываем оси

    # Убираем лишние слоты, если они есть
    for j in range(i, len(axes)):
        fig.delaxes(axes[j])

    plt.tight_layout()
    plt.show()

