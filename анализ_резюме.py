def get_bert_embeddings(text):
    # Encode the text using the tokenizer
    encoded_input = tokenizer(text, return_tensors='pt', max_length=512, truncation=True, padding='max_length')

    # Move encoded input to GPU if available
    if torch.cuda.is_available():
        encoded_input = {key: val.cuda() for key, val in encoded_input.items()}
        model.to('cuda')

    # Compute embeddings
    with torch.no_grad():
        outputs = model(**encoded_input)

    # Retrieve the embeddings for the [CLS] token (first token)
    embeddings = outputs.last_hidden_state[:, 0, :].squeeze()

    # Move embeddings back to CPU if on GPU
    if torch.cuda.is_available():
        embeddings = embeddings.cpu()

    return embeddings.numpy()  # Convert to numpy array

